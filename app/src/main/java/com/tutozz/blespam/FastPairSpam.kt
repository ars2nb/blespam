    lateinit var devicesAdvertiseData: Array<AdvertiseData>
    private var loop = 0
    private val executor = Executors.newSingleThreadExecutor()!!

    init {
        val serviceUUID = ParcelUuid(UUID.fromString("0000FE2C-0000-1000-8000-00805F9B34FB"))
        devicesAdvertiseData = Array(devices.size) { i ->
            val serviceData = Helper.convertHexToByteArray(devices[i].value)
            AdvertiseData.Builder()
                .addServiceData(serviceUUID, serviceData)
                .addServiceUuid(serviceUUID)
                .setIncludeTxPowerLevel(true)
                .build()
        }
    }

    override fun start() {
        executor.execute {
            _isSpamming = true
            for (loop in 0..Helper.MAX_LOOP) {
                if (!_isSpamming) break

                val data = devicesAdvertiseData[Helper.random.nextInt(devices.size)]
                val b = BluetoothAdvertiser()
                b.advertise(data, null)

                try {
                    Thread.sleep(Helper.delay.toLong())
                } catch (e: InterruptedException) {
                    Thread.currentThread().interrupt()
                    return@execute
                }

                b.stopAdvertising()
            }
            _isSpamming = false
        }
    }

    override fun isSpamming(): Boolean = _isSpamming

    override fun stop() {
        _isSpamming = false
    }

